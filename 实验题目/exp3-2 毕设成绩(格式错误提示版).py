try:
    G = float(input("请输入毕设成绩："))
#在上述程序中，将float()改为eval()有一些区别：
#类型转换方式：
#float()函数是将输入转换为浮点数类型。
#eval()函数则会尝试将输入当作有效的Python表达式进行求值，因此它会支持更多种类的输入，不仅限于数字。
#潜在的安全风险：
#eval()函数会执行输入字符串中的任意Python表达式，因此存在安全风险，特别是如果用户输入了恶意代码。
#使用eval()时需要格外小心，避免直接将用户输入用作参数。
#float()函数只会将输入解释为浮点数，不会执行其他任何操作，因此相对来说更安全。
#错误处理：
#如果用户输入的内容无法转换为浮点数，float()函数会抛出ValueError异常，需要在代码中进行适当的异常处理。
#eval()函数在尝试求值用户输入时，如果遇到无法解释的内容，
#会直接引发SyntaxError或NameError等异常，需要相应地进行异常处理。
    if 85 < G <= 100:
        print("优秀")
    if 75 < G <= 84.9:
        print("良好")
    if 66 < G <= 74.9:
        print("中等")
    if 60 < G <= 65.9:
        print("及格")
    if 0.0 < G <= 59.9:
        print("不及格")
except ValueError:
#删除 except 后面的 ValueError 将会使程序捕获所有类型的异常，而不仅限于 ValueError。
#这样做的影响是：
#更广泛的异常捕获：程序不再仅捕获 ValueError 异常，而是捕获任何可能发生的异常。
#这意味着无论是输入格式错误、转换错误还是其他类型的错误，都会被捕获并输出提示信息。
#可能掩盖其他问题：虽然捕获所有异常可以更全面地处理程序可能出现的问题，
#但也可能掩盖其他问题。例如，如果程序中存在其他逻辑错误或语法错误，
#也会被捕获并输出相同的提示信息，这可能会给排查问题带来困难。
    print("输入格式错误，请输入一个数字")
